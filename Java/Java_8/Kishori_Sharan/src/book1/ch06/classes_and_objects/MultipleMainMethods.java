public class MultipleMainMethods {
	public static void main(String[] args) {
		/* May be used as the application entry point */
	}
	
	public static void main(String[] args, int a) {
		/* Another main() method */
	}
	
	public int main() {
		/* Another main() method */
		return 0;
	}
}
