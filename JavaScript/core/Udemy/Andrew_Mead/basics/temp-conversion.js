let fahrenheit= 32

// calculate celsius
let celsius = (fahrenheit - 32) * 5 / 9;
console.log(fahrenheit + " degrees Fahrenheit = " + celsius + " degrees Celsius.")

// calculate kelvin
let kelvin = (fahrenheit + 459.67) * 5 / 9
console.log(fahrenheit + " degrees Fahrenheit = " + kelvin + " degrees Kelvin.")
